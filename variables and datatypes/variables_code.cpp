#include <iostream>
#include <string>
using namespace std;
int main(){
    int myage = 19;               // Integer (whole number without decimals)
    double myheight = 170.59;    // Floating point number (with decimals)
    char myLetter = 'S';         // Character
    string myText = "Hello";     // String (text)
    bool myBoolean = true;       // Boolean (true or false)
    cout << "I am " << myage << " years old.";
    cout << "\nMy name is " << myLetter << "."; 
    cout << "\nMy age is " << myage << "."; 
    cout << "\nMy height is " << myheight << " cm."; 
    cout << "\nMy text is " << myText << "."; 
    cout << "\nMy boolean is " << myBoolean << "."; 

}