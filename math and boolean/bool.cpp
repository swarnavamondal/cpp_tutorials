#include <iostream>
using namespace std;
int main(){
    bool a = true;
    bool b = false;
    cout << "a: " << a << endl;
    cout << "b: " << b << endl;
    int x = 4;
    int y = 5;
    cout << "x == y: " << (x == y) << endl; // false
    cout << "x != y: " << (x != y) << endl; // true
}